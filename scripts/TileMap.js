    draw(canvas,ctx) {
        this.#canvasSize(canvas);
        this.#blankCanvas(canvas,ctx);
        this.#drawMap(ctx);
    }
    //loops over the width and height in order to figure which tile to select in order to 
    #drawMap(ctx){
        for(let row = 0; row < this.map.length; row++){
            for(let column = 0; column < this.map[row].length; column++){
                const tile = this.map[row][column];
                let image = null;
                switch(tile){
                    case 0:
                        image = this.floor;
                        break;
                    case 1:
                        image = this.topWall;
                        break;
                    case 2:
                        image = this.bottomWall;
                        break;
                    case 3:
                        image = this.leftWall;
                        break;
                    case 4:
                        image = this.rightWall;
                        break;
                    case 5:
                        image = this.pathway;
                        break;
                }

                ctx.drawImage(image,column * this.tileSize, row * this.tileSize, this.tileSize, this.tileSize)
            }
        }
    }
    // clears the canvas of any default colors from CSS in order for the JS to draw onto it
    #blankCanvas(canvas,ctx){
        ctx.fillStyle = 'black';
        ctx.fillRect(0,0,canvas.width, canvas.height)
    }
    // determines the size of the game window (canvas) based on the amount of rows and columns in the map array
    #canvasSize(canvas){
        canvas.height = this.map.length * this.tileSize;
        canvas.width = this.map[0].length * this.tileSize;
    }
